PRAGMA foreign_keys = OFF;

BEGIN TRANSACTION;

CREATE TABLE IF NOT EXISTS users (
    id UUID PRIMARY KEY,
    email TEXT NOT NULL UNIQUE,
    password_hash TEXT NOT NULL,
    created_at REAL,
    updated_at REAL
);

CREATE TABLE IF NOT EXISTS sessions (
    id UUID PRIMARY KEY,
    user_id UUID NOT NULL,
    token_hash TEXT NOT NULL UNIQUE,
    expires_at REAL NOT NULL,
    revoked_at REAL,
    created_at REAL,
    updated_at REAL
);

CREATE TABLE IF NOT EXISTS campaign_player_sessions (
    id UUID PRIMARY KEY,
    campaign_id UUID NOT NULL,
    display_name TEXT NOT NULL,
    display_name_normalized TEXT NOT NULL,
    previous_display_names_json TEXT,
    token_hash TEXT NOT NULL UNIQUE,
    expires_at REAL NOT NULL,
    revoked_at REAL,
    created_at REAL,
    updated_at REAL,
    CONSTRAINT "uq:campaign_player_sessions.campaign_id+campaign_player_sessions.display_name_normalized" UNIQUE ("campaign_id", "display_name_normalized")
);

CREATE TABLE IF NOT EXISTS campaigns (
    id UUID PRIMARY KEY,
    name TEXT NOT NULL,
    ruleset_id TEXT NOT NULL,
    is_archived BOOL NOT NULL,
    claim_timeout_minutes INT,
    created_at REAL,
    updated_at REAL
);

CREATE TABLE IF NOT EXISTS campaign_memberships (
    id UUID PRIMARY KEY,
    campaign_id UUID NOT NULL,
    user_id UUID NOT NULL,
    role TEXT NOT NULL,
    created_at REAL,
    updated_at REAL,
    CONSTRAINT "uq:campaign_memberships.campaign_id+campaign_memberships.user_id" UNIQUE ("campaign_id", "user_id")
);

CREATE TABLE IF NOT EXISTS characters (
    id UUID PRIMARY KEY,
    campaign_id UUID NOT NULL,
    owner_id UUID NOT NULL,
    owner_name TEXT NOT NULL,
    last_played_by_name TEXT,
    claimed_session_id UUID,
    claimed_display_name TEXT,
    claimed_at REAL,
    name TEXT NOT NULL,
    initiative REAL,
    reveal_stats BOOL NOT NULL,
    auto_skip_turn BOOL NOT NULL,
    use_app_initiative_roll BOOL NOT NULL,
    initiative_bonus INT NOT NULL,
    is_hidden BOOL NOT NULL,
    reveal_on_turn BOOL NOT NULL,
    created_at REAL,
    updated_at REAL
);

CREATE TABLE IF NOT EXISTS character_stats (
    id UUID PRIMARY KEY,
    character_id UUID NOT NULL,
    stat_key TEXT NOT NULL,
    current_value INT NOT NULL,
    max_value INT NOT NULL,
    created_at REAL,
    updated_at REAL,
    CONSTRAINT "uq:character_stats.character_id+character_stats.stat_key" UNIQUE ("character_id", "stat_key")
);

CREATE TABLE IF NOT EXISTS character_conditions (
    id UUID PRIMARY KEY,
    character_id UUID NOT NULL,
    condition TEXT NOT NULL,
    created_at REAL,
    updated_at REAL,
    CONSTRAINT "uq:character_conditions.character_id+character_conditions.condition" UNIQUE ("character_id", "condition")
);

CREATE TABLE IF NOT EXISTS campaign_encounters (
    id UUID PRIMARY KEY,
    campaign_id UUID NOT NULL UNIQUE,
    encounter_state TEXT NOT NULL,
    round_index INT NOT NULL,
    turn_index INT NOT NULL,
    current_character_id UUID,
    created_at REAL,
    updated_at REAL
);

CREATE TABLE IF NOT EXISTS _fluent_migrations (
    id UUID PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    batch INT NOT NULL,
    created_at REAL,
    updated_at REAL
);

